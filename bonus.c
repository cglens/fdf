#include "fdf.h"

void	ft_get_color(t_graph *draw, int z0, int z1)
{
	if (z0 + z1 < 0)
		draw->color = 0x0000ff;
	else if (z0 + z1 > 0)
		draw->color = 0xff0000;
	else
		draw->color = 0x00ff00;
}
